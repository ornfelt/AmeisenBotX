namespace AmeisenBotX.Core.Objects.Enums
{
    /// <summary>
    /// Represents the different types of non-playable characters (NPCs) in a game.
    /// </summary>
    public enum NpcType
    {
        /// <summary>
        /// Sets the VendorSellBuy property to 0.
        /// </summary>
        VendorSellBuy = 0,
        /// <summary>
        /// Represents the vendor repair option setting as 1.
        /// </summary>
        VendorRepair = 1,
        /// <summary>
        /// Defines the ClassTrainer as 2.
        /// </summary>
        ClassTrainer = 2,
        /// <summary>
        /// The ProfessionTrainer field represents a value of 3, indicating that the entity is a professional trainer.
        /// </summary>
        ProfessionTrainer = 3,
        /// <summary>
        /// The Auctioneer has a value of 4.
        /// </summary>
        Auctioneer = 4,
        /// <summary>
        /// The Banker variable represents the value 5.
        /// </summary>
        Banker = 5,
        /// <summary>
        /// The BattleMaster is assigned a value of 6.
        /// </summary>
        BattleMaster = 6,
        /// <summary>
        /// Represents a FlightMaster with a value of 7.
        /// </summary>
        FlightMaster = 7,
        /// <summary>
        /// The WeaponMaster variable is assigned a value of 8.
        /// </summary>
        WeaponMaster = 8
    }
}